from datetime import datetime, timezone
from pathlib import Path


def render_filelist(
    include_dirs: list[Path],
    package_files: list[Path],
    source_files: list[Path],
    top_module: str | None = None,
    top_directive: str | None = None,
    simulator: str = "icarus",
    iterations: int = 0,
    no_comments: bool = False,
) -> str:
    """Render the contents of a .f filelist."""
    ts = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")
    lines: list[str] = []

    if not no_comments:
        lines.append(f"// Generated by vcodeman gen — {ts}")
        lines.append(f"// Simulator: {simulator} | Iterations: {iterations}")
        lines.append("")

    for d in include_dirs:
        lines.append(f"+incdir+{d}")

    if include_dirs and not no_comments:
        lines.append("")

    if package_files:
        if not no_comments:
            lines.append("// --- packages ---")
        for f in package_files:
            lines.append(str(f))
        if not no_comments:
            lines.append("")

    if source_files:
        if not no_comments:
            lines.append("// --- sources (topological order) ---")
        for f in source_files:
            lines.append(str(f))

    if top_module:
        lines.append("")
        if top_directive:
            lines.append(top_directive)
        else:
            lines.append(f"// -top {top_module}")

    return "\n".join(lines)
